export function cleanLocationName(name: string): string {
  return name
    .replace(/[\u{1F1E6}-\u{1F1FF}\u{1F300}-\u{1FAFF}\u{2600}-\u{27BF}]/gu, "")
    .replace(/\(.*?\)/g, "")
    .trim();
}

export function getLocationCoordinates(name: string): [number, number] | null {
  const cleaned = cleanLocationName(name);
  const key = Object.keys(LOCATION_COORDINATES).find((candidate) => {
    const cleanedCandidate = cleanLocationName(candidate);
    return cleaned.includes(cleanedCandidate) || cleanedCandidate.includes(cleaned);
  });

  return key ? LOCATION_COORDINATES[key] : null;
}

export function hasKnownLocation(name: string | undefined): boolean {
  return !!name && getLocationCoordinates(name) !== null;
}
